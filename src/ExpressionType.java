/**
 * 式の種類を表します。
 */
public enum ExpressionType {
    /**
     * 被演算子を表します。
     */
    OPERAND,
    /**
     * 単項演算子を表します。
     */
    UNARY_OPERATOR,
    /**
     * 二項演算子を表します。
     */
    BINARY_OPERATOR,
    /**
     * 比較演算子を表します。
     */
    COMPARSION_OPERATOR,
     /**
     * 代入演算子を表します。
     */
    ASSAIGNMENT_OPERATOR,
    /**
     * 終わりを表します。
     */
    END
}
